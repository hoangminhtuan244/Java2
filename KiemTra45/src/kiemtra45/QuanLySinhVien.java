    private void displayAllStudentsInTable() {
        tableModel.setRowCount(0);
        
        for (Student student : studentList) {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, student.getFullName(), student.getGender(), student.getRollNo(), student.getEmail(), student.getAddress()});
        }
    }
    
    
