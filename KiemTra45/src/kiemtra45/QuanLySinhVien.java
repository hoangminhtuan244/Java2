    private void selectDB(String rollNo){
        try {
            studentList.clear();
            //Create connection
            
            Connection conn = DriverManager.getConnection(DB_URL, USER_NAME, PASSWORD);
            
            //query : insert, update, delete
            String sql = "SELECT * FROM student WHERE rollNo_sv = '"+ rollNo +"'";
            
            // crate statement to insert student
            PreparedStatement stmt = null;
            
            // select all student
            stmt = conn.prepareStatement(sql);
            // get data from table 'student'
            ResultSet rs = stmt.executeQuery();
            // show data
            
            while(rs.next()) {
                int id = rs.getInt("id_sv");
                String fName = rs.getString("fullname_sv");
                String sex = rs.getString("gender_sv");
                String rollno = rs.getString("rollNo_sv");
                String email = rs.getString("email_sv");
                String address = rs.getString("address_sv");
                
                Student std = new Student(id, fName, sex, rollno, email, address);
                studentList.add(std);
            }
            
            //close connection
            stmt.close();
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
