    private void getData() {
        try {
            list.clear();
            //Create connection
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cate", "root", "");
            
            //query : insert, update, delete
            String sql = "select * from cate";
            Statement statement = conn.createStatement();
            ResultSet result = statement.executeQuery(sql);
            
            while(result.next()) {
                int id = result.getInt("id");
                String name = result.getString("catename");
                
                Cate cate = new Cate(id, name);
                list.add(cate);
            }
            
            //close connection
            statement.close();
            conn.close();
        } catch (Exception ex) {
            Logger.getLogger(MainForm1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void displayData() {
        tableModel.setRowCount(0);
        
        for (Cate list1 : list) {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, list1.getCateName()});
        }
    }
    
