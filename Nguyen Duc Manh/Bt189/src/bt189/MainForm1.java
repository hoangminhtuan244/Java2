    private void insert(Cate cate){
        
        try {
            //Create connection
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cate", "root", "");
            
            //query : insert, update, delete
            String sql = "";
            if(cate.getId() > 0) {
                sql = "update cate set cate =? where id = " + cate.getId();
            } else {
                sql = "insert into student(cate) values (?)";
            }
            
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, cate.getCateName());
            
            statement.execute();
            
            //close connection
            statement.close();
            conn.close();
        } catch (Exception ex) {
            Logger.getLogger(MainForm1.class.getName()).log(Level.SEVERE, null, ex);
        }
        getData();
        displayData();
    }
    
